// Vamos calcular o número digitado em busca do resto da divisão
// Se o resto for igual a 0 (zero), então o número digitado é Par
// caso contrário, será impar

//const numero = parseInt(prompt("Digite um número"));
const numero = prompt("Digite um número");

if (numero % 2 == 0) {
    alert("Número Par")
} else {
    alert("Número Impar")
}

